function set(obj, keyPath, value) {
  const parts = keyPath.split('.');
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    if (!cur[parts[i]] || typeof cur[parts[i]] !== 'object') cur[parts[i]] = {};
    cur = cur[parts[i]];
  }
  cur[parts[parts.length - 1]] = value;
}

function walk(node, prefix = '') {
  const leaves = [];
  for (const [k, v] of Object.entries(node)) {
    const p = prefix ? `${prefix}.${k}` : k;
    if (v && typeof v === 'object' && !v.tr) {
      leaves.push(...walk(v, p));
    } else if (v && v.tr) {
      leaves.push({ path: p, values: v });
    }
  }
  return leaves;
}

const leaves = walk(patches);
for (const loc of ['tr', 'en', 'de', 'az', 'ru', 'uk', 'ar']) {
  const file = path.join(dir, `${loc}.json`);
  const data = JSON.parse(fs.readFileSync(file, 'utf8'));
  for (const leaf of leaves) set(data, leaf.path, leaf.values[loc]);
  fs.writeFileSync(file, JSON.stringify(data, null, 2) + '\n');
}

